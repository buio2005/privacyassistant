  function getPageName() {
    const fileName = globalObject.location.pathname.split("/").pop() || "index.html";
    return fileName.replace(".html", "");
  }

  function interpolate(text, variables) {
    return Object.keys(variables || {}).reduce((result, key) => {
      return result.replace(new RegExp(`\\{${key}\\}`, "g"), String(variables[key]));
    }, String(text));
  }

  function detectDefaultLanguage() {
    const browserLanguage = String((globalObject.navigator && globalObject.navigator.language) || "it").toLowerCase();
    return browserLanguage.startsWith("it") ? "it" : "en";
  }

  function getCurrentLanguage() {
    const stored = globalObject.localStorage.getItem(LANGUAGE_KEY);
    if (SUPPORTED_LANGUAGES.includes(stored)) {
      return stored;
    }
    return detectDefaultLanguage();
  }

  function setCurrentLanguage(language) {
    const normalizedLanguage = SUPPORTED_LANGUAGES.includes(language) ? language : "it";
    globalObject.localStorage.setItem(LANGUAGE_KEY, normalizedLanguage);
  }

