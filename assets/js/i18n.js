  function applyStaticTranslations() {
    const language = getCurrentLanguage();
    const pageName = getPageName();
    document.documentElement.lang = language;

    if (language !== "en") {
      document.title = document.title;
      return;
    }

    const pageConfig = STATIC_TRANSLATIONS[pageName];
    if (!pageConfig) {
      return;
    }

    document.title = pageConfig.title;
    const descriptionNode = document.querySelector('meta[name="description"]');
    if (descriptionNode) {
      descriptionNode.setAttribute("content", pageConfig.description);
    }

    pageConfig.items.forEach((item) => {
      const nodes = document.querySelectorAll(item.selector);
      if (!nodes.length) {
        return;
      }
      const node = typeof item.index === "number" ? nodes[item.index] : nodes[0];
      if (!node) {
        return;
      }
      node.textContent = item.text;
    });
  }

  function injectLanguageSwitcher() {
    const navLists = document.querySelectorAll(".app-navbar .navbar-nav");
    if (!navLists.length || document.querySelector("[data-language-switch]")) {
      return;
    }

    navLists.forEach((navList) => {
      const item = document.createElement("li");
      item.className = "nav-item ms-lg-2 language-switch-item";
      item.innerHTML = `
        <select class="form-select form-select-sm language-switch" data-language-switch aria-label="${t("common.languageSwitchAria", "Cambia lingua")}">
          <option value="it">ITA</option>
          <option value="en">ENG</option>
        </select>
      `;
      const themeItem = navList.querySelector(".theme-toggle")?.closest("li");
      if (themeItem) {
        navList.insertBefore(item, themeItem);
      } else {
        navList.appendChild(item);
      }
    });

    document.querySelectorAll("[data-language-switch]").forEach((selectNode) => {
      selectNode.value = getCurrentLanguage();
      selectNode.addEventListener("change", (event) => {
        setCurrentLanguage(event.target.value);
        globalObject.location.reload();
      });
    });
  }

  function updateThemeAccessibilityLabel() {
    const themeToggle = document.querySelector("[data-theme-toggle]");
    if (themeToggle) {
      themeToggle.setAttribute("aria-label", t("theme.toggleAria", "Cambia tema"));
    }
  }

  document.addEventListener("DOMContentLoaded", () => {
    injectLanguageSwitcher();
    applyStaticTranslations();
    updateThemeAccessibilityLabel();
  });

